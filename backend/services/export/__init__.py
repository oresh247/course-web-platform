def normalize_newlines(text: str) -> str:
    """Нормализует переносы строк в тексте.

    Заменяет литеральные последовательности \\n (два символа: обратный слеш + n)
    на реальные переносы строк. Это необходимо, потому что AI-генерированный
    контент или данные из БД могут содержать экранированные переносы строк
    вместо реальных символов новой строки.

    Args:
        text: Исходный текст с возможными литеральными \\n.

    Returns:
        str: Текст с нормализованными переносами строк.
    """
    if not text:
        return text
    # Заменяем литеральные \n (два символа) на реальные переносы строк
    return text.replace('\\n', '\n')
